// Basic example of use of variables in Rust
fn main() {
    // Declare and initialize a variable of type integer
    let x = 5;

    // Declare and initialize a variable of type string
    let y = "Hello, world!";

    // Declare and initialize a variable of type boolean
    let z = true;

    // Print the values of the variables
    println!("The value of x is: {}", x);
    println!("The value of y is: {}", y);
    println!("The value of z is: {}", z);
}
